'use client'


import { Button } from '@/components/ui/button'
import { cn } from '@/lib/utils'
import { SparkleIcon } from 'lucide-react'
import { Poppins } from 'next/font/google'
import {FaGithub} from 'react-icons/fa'
import ProjectList from './ProjectList'
import { useCreateProject } from '@/hooks/use-projects'
import {adjectives, animals, colors, uniqueNamesGenerator} from 'unique-names-generator'
import { useEffect, useState } from 'react'
import ProjectCommandDialog from './components/projects-command-dialog'



const font = Poppins({
    subsets: ['latin'],
    weight: ["400", "500", '600']
})

const ProjectView = () => {

    const [commandDialogOpen, setCommandDialogOpen] = useState(false)

    useEffect(()=>{
        const handleKeyDown = (e:KeyboardEvent)=>{
           
            if(e.metaKey || e.ctrlKey){
                if(e.key === 'k'){
                    e.preventDefault();
                    setCommandDialogOpen(true)
                }
            }

        }
        document.addEventListener('keydown', handleKeyDown);

        return (

            document.removeEventListener('keydown', handleKeyDown)
        )

    },[])

    const createProject = useCreateProject()


    return (
        <>
        <ProjectCommandDialog
         open={commandDialogOpen}
         onOpenChange={setCommandDialogOpen}
         />
        <div className='min-h-screen bg-sidebar flex flex-col items-center justify-center p-6 md:p-6'>
            <div className='w-full max-w-sm mx-auto flex flex-col gap-4 items-center'>
                <div className='flex justify-between gap-4 w-full items-center'>
                    <div className='flex items-center gap-2 w-full group/logo'>
                    <img src={"/logo.svg"} alt='Polaris' className='size-8 md:size-11.5' />
                        <h1 className={cn('text-4xl md:text-5xl font-semibold', font.className)}>
                            Polaris
                        </h1>
                    </div>
                </div>
                <div className='flex flex-col gap-4 w-full'>
                    <div className='grid grid-cols-2 gap-2'>
                        <Button

                        
                        onClick={()=>{
                        
                            const ProjectName= uniqueNamesGenerator({
                                dictionaries:[adjectives, animals, colors],
                                separator: "-",
                                length:3,
                            })
                            
                            createProject({
                                name: ProjectName
                            })
                        }}
                        variant={'outline'}
                        className='h-full items-start justify-start p-4 bg-background border flex flex-col gap-6 rounded-none'
                        >
                            <div className='flex items-center justify-between w-full'>
                              <SparkleIcon className='size-4' />
                                <kbd className='bg-accent border'>
                                   Ctrl + J
                                </kbd>
                            </div>
                            <span>New</span>
                        </Button>

                         <Button 
                        variant={'outline'}
                        className='h-full items-start justify-start p-4 bg-background border flex flex-col gap-6 rounded-none'
                        >
                            <div className='flex items-center justify-between w-full'>
                              <FaGithub className='size-4' />
                                <kbd className='bg-accent border'>
                                   Ctrl + I
                                </kbd>
                            </div>
                            <span>Import</span>
                        </Button>
                    </div>
                     <ProjectList onViewAll={()=> setCommandDialogOpen(true)}/> 
                </div>
            </div>
        </div>
                        </>
    )
}

export default ProjectView
